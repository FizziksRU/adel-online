// Геометрия схемы «Фобоса» для отрисовки карты.
// Вынесено из Board.jsx отдельным модулем без JSX, чтобы раскладку можно было
// проверять тестом (test/layout.js) — линия перехода, прошедшая сквозь чужую
// локацию, читается как несуществующая связь и вводит игроков в заблуждение.

// Корабль устроен как два кольца, сходящиеся в локации 9 (центральный узел):
//   верхнее  9-4-3-2-6-7-8-9           с тупиками 1 (от 2) и 5 (от 6)
//   нижнее   9-10-14-15-12-19-18-11-9  с тупиковыми ветками 13-16 и 17-20
// Карта развёрнута на 90° против часовой относительно поля: нос корабля
// (тупики 1 и 5) слева, корма с локацией 12 справа. Поэтому пары секторов
// легли по горизонтали: зелёный и красный сверху, жёлтый и синий снизу.
// Люковые локации 3 и 7 вынесены наружу — так силуэт корпуса читается лучше.
// ВАЖНО: не ставьте три локации подряд в одну колонку или строку — переход
// между крайними пройдёт сквозь среднюю. Тест это ловит.
export const POS = {
  1: [0, 1], 2: [1, 1], 3: [2, 0.6], 4: [3, 1],         // зелёный
  5: [0, 3], 6: [1, 3], 7: [2, 3.4], 8: [3, 3],         // жёлтый
  9: [4, 2], 10: [5, 1], 11: [5, 3], 12: [8, 2],        // серый
  13: [6, 0], 14: [6, 1.5], 15: [7, 1.5], 16: [7, 0],   // красный
  17: [6, 4], 18: [6, 2.5], 19: [7, 2.5], 20: [7, 4],   // синий
};

// Коробки локаций укрупнены относительно ячейки (решение владельца: крупнее всё
// на карте). Ячейка чуть выросла, коробка — заметно сильнее, поэтому пустого
// места между коробками меньше и всё внутри читается крупнее. Отношения
// BOX/CELL стережёт layout-тест (перекрытия и пересечения линий переходов).
export const CELL_X = 128, CELL_Y = 112, PAD = 28, BOX_W = 112, BOX_H = 94;

// Левый верхний угол коробки локации в координатах SVG.
export const xy = (l) => [PAD + POS[l][0] * CELL_X, PAD + POS[l][1] * CELL_Y];
// Центр коробки — отсюда рисуются линии переходов.
export const center = (l) => { const [x, y] = xy(l); return [x + BOX_W / 2, y + BOX_H / 2]; };
export const gridMax = (i) => Math.max(...Object.values(POS).map(p => p[i]));
